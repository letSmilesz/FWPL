void PrintPicture(int[,] arr)
{
    for (int i = 0; i < arr.GetLength(0); i++)
    {
        for (int j = 0; j < arr.GetLength(1); j++)
        {
            if (arr[i, j] == 1) Console.Write(arr[i, j]);
            else Console.Write(" ");
        }
        Console.WriteLine();
    }
}


void PaintPicture(int x, int y)
{
    if (pic[x, y] == 0)
    {
        pic[x, y] = 1;
        PaintPicture(x, y + 1);
        PaintPicture(x - 1, y);
        PaintPicture(x, y - 1);
        PaintPicture(x + 1, y);
    }
}

